          <article class="text-item">
            <h2>A reciclagem e o meio ambiente</h2>
            <p class="text-p">Uma das principais preocupações dos centros urbanos é a quantidade do lixo produzido pela população. Esta questão representa um dos maiores desafios a ser enfrentado pelas administrações públicas, pois além dos problemas relacionados aos catadores dos lixões, à falta de espaço para disposição dos resíduos, deve também ser levada em conta a preservação do meio ambiente.<br />
              A atitude de reciclar, além de diminuir a quantidade de lixo a ser tratada e eliminada, contribui significativamente para a redução da extração de matérias-primas necessárias à produção de novos bens de consumo. Afinal, adotar a educação ambiental, colocando os resíduos recicláveis nos locais devidos, não nos custa nada e ainda promove uma melhor qualidade de vida para toda a população.<br />
            Ter uma vida mais saudável depende tanto de uma política pública de serviços ambientalmente adequados de limpeza urbana quanto da atitude da população. Poderemos verificar uma notável diferença nos resultados finais deste investimento, partindo da prática da teoria dos 3 R’s, que significam: Redução, do uso de matéria-prima e energia e do desperdício nas fontes geradoras, Reutilização dos materiais e a Reciclagem.</p>

            <div class="recicle-item">
              <img src="img/bg-12.jpg"/>
              <p class="text-p">Esta técnica de separação dentro da metodologia dos 3 R’s, se torna cada vez mais eficiente e imprescindível para um modo de vida mais saudável e responsável. Pois, além da preservação dos recursos naturais do meio ambiente, a reciclagem se torna socialmente importante, desde o momento e que gera emprego e renda para catadoresde rua e dos lixões.</p>
            </div>
          </article>

          <article class="text-item reciclagem">
            <h2>O que é a reciclagem?</h2>
            <div class="reciclagem-item">
              <img src="img/bg-13.jpg" alt="">
              <p class="text-p">A reciclagem é o resultado de um conjunto de técnicas e atividades que tem o objetivo de reaproveitar e reutilizar os resíduos de substâncias em seus ciclos de produção. Hoje, já se encontram várias alternativas de reaproveitamento destes materiais em confecções de produtos artesanais, vestuário, acessórios, etc, dos lixões.</p>
            </div>
          </article><!--fim da article-->


          <article class="text-item import-rec">
            <h2>Qual a importância da reciclagem?</h2>

            <div class="import-rec-item">
              <img src="img/bg-27.png" alt="">
              <p class="text-p">Cada vez mais se faz necessário o cuidado e a atenção com o meio ambiente. O desequilíbrio provocado pela devastação de recursos naturais está colocando em risco, não só espécies animais e vegetais, mas a sobrevivência do próprio homem no planeta. Uma das formas de revertermos esta situação é o reaproveitamento de materiais recicláveis, evitando uma maior extração de recursos e diminuindo o acúmulo de lixo nas áreas urbanas.</p>
            </div> 
          </article>



          <article class="text-item">
            <h2>Quais as vantagens da reciclagem?</h2>
            <p class="text-p">
              <b>✓ </b>Redução da quantidade de resíduos encaminhados ao aterro sanitário com conseqüente aumento da sua vida útil;<br />
              <b>✓ </b>Redução dos impactos ambientais durante a produção de novas matérias primas;<br />
              <b>✓ </b>Redução no consumo de energia elétrica;<br />
              <b>✓ </b>Redução da poluição do ambiental;<br />
              <b>✓ </b>Ampliação do desenvolvimento econômico pela geração de novos empregos na operacionalização dos materiais recicláveis e na expansão dos negócios relativos à reciclagem.
            </p>
          </article>


          <article class="text-item separacao">
            <h2>Como deve ser a separação do lixo?</h2>

            <div class="seperacao-item">
              <img src="img/bg-15.jpg" alt="">                    
              </div>
                    <div class="separacao-sub">
                        <p class="text-p">
                          <strong>1º passo:</strong>
                          Devemos separar todo o material que pode ser reciclado do que não pode, ou seja, vidros, papéis, plásticos e metais devem ser colocados em outro saco ou container de lixo. É importante lembrar que o lixo orgânico não deve ser colocado junto do lixo reciclável apesar de também pode ser reutilizado.
                        </p>

                        <p class="text-p">
                          <strong>2º passo:</strong>
                          Todo o material reciclável, anteriormente separado, deve ser lavado e estar seco para que possa ser reciclado.
                        </p>

                        <p class="text-p">
                          <strong>3º passo:</strong>
                          Com o material reciclável limpo e separado, basta depositá-lo em um local estratégico e diferente do local onde se deposita o lixo comum, para que seja fácil o recolhimento. Vale lembrar que fica muito mais prático colocar o lixo reciclável em um saco plástico transparente, tanto para quem recolhe quanto para quem separa o lixo.<br />
                          Antes de colocar o lixo na calçada deve-se verificar o dia exato que a coleta seletiva de lixo faz o recolhimento no seu bairro.
                        </p>
                    </div>


          </article>

          <article class="text-item necessario">
            <h2>O que é necessário para o síndico do meu prédio implantar a separação de recicláveis no meu condomínio?</h2>
            <div class="necessario-item">
              <img src="img/bg-16.jpg" alt="">
              <p class="text-p">
               Para implantar a separação de recicláveis em um condomínio, seja ele de casas ou apartamentos, basta especificar um local para a colocação de contêineres para cada material e logo após comunicar a todos os moradores.
             </p>
           </div>
         </article>

         <article class="text-item">
          <h2>E se no meu condomínio não houver espaço para os contêineres?</h2>
          <p class="text-p">Não há problema nenhum. A separação dos materiais recicláveis pode ser feita e armazenada dentro da própria residência até o dia da coleta. Já que os resíduos estarão limpos, não produzirão mau cheiro. Porém, o síndico ou o funcionário responsável deve orientar os moradores para colocar a sacola de lixo reciclável para ser recolhida somente no dia da passagem do caminhão. Assim, evita-se que se acumulem lixos nas calçadas e que alguém mexa no lixo, o que é proibido.</p>
        </article>


        <article class="text-item">
          <h2>Onde armazenar o lixo até a coleta?</h2>
          <p class="text-p">Para que não ocupe muito espaço, o lixo deve ser compactado, ou seja, os papéis devem ser colocados em pilhas, as latinhas amassadas, as garrafas pet (plásticos), por exemplo, colocadas separadas, assim como os vidros. Em seguida, o lixo deve ficar dentro de sacos de plástico resistente, ou no caso das pilhas de papel, amarrada com barbante ou outra fita resistente, e armazenado em local fechado e ao abrigo de sol e chuva até o dia da coleta.</p>
        </article>

        <article class="text-item condominio">
          <h2>Caso o meu condomínio não adote o procedimento de separação de lixo reciclável, eu posso fazer isso sozinho? Como?</h2>
          <div class="condominio-item">
            <img src="img/bg-17.jpg" alt=""/>
            <p class="text-p">
             Claro que pode. O fato de o síndico do seu prédio não aderir a este procedimento não interfere em nada na sua escolha. Como em resposta anterior, o lixo reciclável pode ser armazenado dentro da sua residência, por não exalar mau cheiro, até o dia que a coleta seletiva for passar na sua rua.
           </p>
         </div>
       </article>

       <article class="text-item lixo">
        <h2>Quem recolherá este lixo?</h2>
        <div class="lixo-item">
          <img class="img" src="img/bg-18.jpg" alt="" />
          <p class="text-p">
           A própria Comlurb fará o recolhimento, que será realizado em um dia diferente do recolhimento do lixo comum. Um caminhão compactador fará a coleta (uma vez por semana ou duas, dependendo da área) e levará o material reciclável para uma das Central de Separação de Recicláveis (CSR´s).
         </p>
       </div>
     </article>

     <article class="text-item">
      <h2>O que são Centrais de Separação de Recicláveis – CSR?</h2>
      <p class="text-p">As CSR’s são os locais para onde os lixos recicláveis são levados para serem separados de acordo com o material de sua composição. Atualmente existem duas no Rio de Janeiro: CSR Botafogo que recebe todo o lixo reciclável da coleta seletiva dos bairros da Zona Sul, mais Grajaú e Santa Tereza e CSR Vargem Pequena que recebe todo o lixo reciclável da coleta seletiva da Barra, Recreio e Jacarepaguá.</p>
    </article>

    <article class="text-item caminhao">
